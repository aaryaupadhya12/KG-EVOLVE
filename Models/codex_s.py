EPISODIC_TSV = "episodic_memory.tsv"

def extract_reasoning_patterns(record: dict, agg: dict) -> list[tuple]:
    """
    Extract reusable relational patterns from a resolved case.

    Instead of storing the answer, we store:
    (head, discriminative_relation, correct_tail)

    These are actual KG-grounded triples that encode WHY the
    correct tail was chosen — the separating signal — not just
    what was chosen.

    Only patterns grounded in only_tail_has are stored,
    because those are the relations that structurally separate
    the true tail from the predicted (wrong) entity.
    """
    patterns = []

    head        = record["head"]
    true_tail   = record["tail"]
    only_tail   = set(record.get("only_tail_has", []))
    selected    = set(agg.get("selected_relations", []))
    subgraph    = record.get("subgraph", [])

    # only relations that are BOTH:
    # 1. cited by the winning agent as discriminative
    # 2. actually exist in the subgraph as a real triple
    # 3. are separating signals (in only_tail_has)
    grounded_discriminative = selected & only_tail

    # build a lookup of what triples actually exist in subgraph
    subgraph_triples = {(h, r, t) for h, r, t in subgraph}

    for rel in grounded_discriminative:
        # verify this triple actually exists in the local graph
        # — prevents storing hallucinated relations
        if (head, rel, true_tail) in subgraph_triples:
            patterns.append((head, rel, true_tail))
        else:
            # try the reverse — maybe it's a tail→head edge
            if (true_tail, rel, head) in subgraph_triples:
                patterns.append((true_tail, rel, head))

    return patterns


def writeback(agg: dict, record: dict):
    """
    Quality-gated writeback that stores only:
    - Resolved cases (agent got it right)
    - Relations that are both agent-cited AND structurally
      discriminative (in only_tail_has)
    - Triples that actually exist in the local subgraph
      (grounded, not hallucinated)

    Result: episodic memory contains real KG triples that
    encode discriminative relational patterns, not meta-logs.
    """
    # gate 1 — only store resolved cases
    if agg.get("failure_type") != "resolved":
        return
    if agg.get("final_answer") != record.get("tail"):
        return

    # gate 2 — extract grounded patterns
    patterns = extract_reasoning_patterns(record, agg)

    if not patterns:
        # agent got it right but cited no discriminative
        # subgraph-grounded relations → lucky prediction
        # do NOT store — this is exactly the noise you want
        # to exclude
        print(f"  [TSV] skipped — lucky prediction, "
              f"no grounded discriminative relations")
        return

    # write clean triples
    new_file = not os.path.exists(EPISODIC_TSV)
    with open(EPISODIC_TSV, "a", newline="") as f:
        w = csv.writer(f, delimiter="\t")
        if new_file:
            w.writerow(["head", "relation", "tail"])
        w.writerows(patterns)

    print(f"  [TSV] {len(patterns)} grounded triples → "
          f"{EPISODIC_TSV}")

    # FAISS episodic — store the pattern as retrievable text
    # format: "head --rel--> tail" for semantic search
    for h, r, t in patterns:
        write_episodic(
            h, r, t,
            agg.get("chosen_agent", "?"),
            f"discriminative: {h} --{r}--> {t}"
        )

    # FAISS semantic — store relation-level pattern
    # for cross-entity generalisation
    write_semantic(
        agg.get("chosen_agent", "?"),
        record.get("relation", "?"),   # query relation
        list({r for _, r, _ in patterns})
    )

