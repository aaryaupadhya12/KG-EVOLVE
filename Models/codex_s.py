def route(record):
    """
    Agent B handles all multi-hop failures — which is all test triples.
    Agent A runs as secondary check on near-misses only.
    Returns primary agent and whether to run secondary.
    """
    rank     = record["true_rank"]
    hop_type = record["hop_type"]

    # primary routing
    if hop_type == "multi" or hop_type == "none":
        primary = "B"   # path reasoning for all multi-hop
    else:
        primary = "A"   # similarity for direct edge failures

    # secondary check — run A on near misses regardless
    secondary = "A" if rank <= 6 and primary == "B" else None

    return primary, secondary

