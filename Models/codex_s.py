def extract_subgraph(entity, graph, hops=4):
    """
    Pulls all triples within N hops of entity.

    hop=1: only direct edges from entity
    hop=2: direct edges + edges from those neighbors

    This becomes Agent B's local context.
    """
    subgraph = []
    visited  = set()

    def walk(node, depth):
        if depth == 0 or node in visited:
            return
        visited.add(node)
        for relation, tail in graph.get(node, []):
            subgraph.append((node, relation, tail))
            walk(tail, depth - 1)

    walk(entity, hops)
    return subgraph

# now — extract focused subgraph around the query entities
# for probe: (usa, militaryalliance, uk) vs netherlands
def focused_subgraph(entities, graph, hops=1):
    """
    Extracts subgraph around multiple entities.
    Filters to only show triples connecting them.

    This is the ~30 triple context you pass to the LLM.
    """
    all_triples = []
    for entity in entities:
        all_triples.extend(
            extract_subgraph(entity, graph, hops)
        )

    # deduplicate
    seen    = set()
    unique  = []
    for triple in all_triples:
        key = (triple[0], triple[1], triple[2])
        if key not in seen:
            seen.add(key)
            unique.append(triple)

    return unique

