def get_entity_embeddings(model):
    embs = model.entity_representations[0](
        indices=None
    ).detach().cpu()

    # RotatE uses complex numbers — convert to real magnitude
    if embs.is_complex():
        embs = embs.abs()

    return embs

def similarity_summary(entity, model, k=5):
    embs  = get_entity_embeddings(model)
    e_id  = entity_to_id[entity]
    e_vec = embs[e_id]

    sims  = F.cosine_similarity(
        e_vec.unsqueeze(0), embs
    ).detach().cpu().numpy()

    ranked  = np.argsort(sims)[::-1]
    results = []

    for idx in ranked:
        name = id_to_entity[idx]
        if name == entity:
            continue
        score = sims[idx]

        entity_rels   = set(
            df_train[df_train["tail"] == entity]["relation"]
        )
        neighbor_rels = set(
            df_train[df_train["tail"] == name]["relation"]
        )
        shared = len(entity_rels & neighbor_rels)

        results.append((name, score, shared))
        if len(results) == k:
            break

    parts   = [
        f"{name}(sim={score:.2f}, shared_rels={shared})"
        for name, score, shared in results
    ]
    summary = f"{entity} most similar to: {', '.join(parts)}"

    return summary, results

def hop_classifier(head, tail, graph, target_relation=None):
    """
    If target_relation is specified:
      single = direct edge exists with THAT relation
      multi  = reachable but via different relations
    """
    # check direct edge with matching relation
    for relation, t in graph.get(head, []):
        if t == tail:
            if target_relation is None or relation == target_relation:
                return "single", 1, relation

    # direct edge exists but wrong relation
    direct_wrong = [r for r, t in graph.get(head, []) if t == tail]
    if direct_wrong:
        return "multi", 1.5, f"direct but via {direct_wrong[0]}"

    # check 2-hop
    paths_found = []
    for r1, mid in graph.get(head, []):
        for r2, t2 in graph.get(mid, []):
            if t2 == tail:
                paths_found.append(
                    f"{head}-{r1}->{mid}-{r2}->{tail}"
                )

    if paths_found:
        return "multi", 2, paths_found[0]

    return "none", 99, []

def get_full_ranking_filtered(head, relation, true_tail, model):
    """
    Scores all entities as candidate tails for (head, relation, ?).
    Filters out the head entity itself (self-loop).
    Returns rank of true_tail plus full sorted ranking.
    """
    h_id = entity_to_id[label_to_entity.get(head, head)]
    r_id = relation_to_id[label_to_relation.get(relation, relation)]

    all_scores = []
    for i in range(len(entity_to_id)):
        # skip self-prediction
        if id_to_entity[i] == head:
            continue
        score = model.score_hrt(
            torch.tensor([[h_id, r_id, i]]).to(device)
        ).item()
        all_scores.append((id_to_entity[i], score, i))

    all_scores.sort(key=lambda x: -x[1])

    ranked_entities = [e for e, s, i in all_scores]
    true_rank       = ranked_entities.index(true_tail) + 1
    predicted       = all_scores[0][0]

    return {
        "predicted":       predicted,
        "predicted_score": all_scores[0][1],
        "true_tail":       true_tail,
        "true_score":      next(s for e, s, i in all_scores if e == true_tail),
        "true_rank":       true_rank,
        "full_ranking":    [(e, round(s, 3)) for e, s, i in all_scores],
    }

