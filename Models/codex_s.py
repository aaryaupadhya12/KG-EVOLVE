print(f"Already done:         {len(held_already_done)}")
print(f"Remaining:            {len(held_remaining)}")

# ── load TSV memory from training writeback ───────────────
tsv_memory = load_tsv_memory("/kaggle/input/datasets/aaryaupi/tsv-file/episodic_memory (21).tsv")
print(f"TSV memory loaded:    {len(tsv_memory)} entities")

held_all_results = list(held_checkpoint.values())

# ── run pipeline ──────────────────────────────────────────
for i, record in enumerate(held_remaining):
    key = triple_key(record)
    print(f"\n[{i+1}/{len(held_remaining)}]  {key}  rank={record['true_rank']}")

    try:
        context = build_agent_context(record, tsv_memory=tsv_memory)  # ← CHANGED

        results = {}
        def _run_a():
            results["A"] = agent_a(context, record)
        def _run_b():
            time.sleep(AGENT_A_B_DELAY)
            results["B"] = agent_b(context, record)

        with ThreadPoolExecutor(max_workers=2) as ex:
            fa = ex.submit(_run_a)
            fb = ex.submit(_run_b)
            fa.result(); fb.result()

        a_out, b_out = results["A"], results["B"]

        # ── inference scoring — no true_tail, no record passed ──
        s_a = inference_score(a_out, "A",graph = graph)
        s_b = inference_score(b_out, "B", graph = graph )

        # ── inference aggregation — clean routing + LLM labelling ──
        agg = inference_aggregate(a_out, b_out, s_a, s_b, record)

        # ── correctness checked post-hoc only, never fed back in ──
        correct = (
            agg.get("final_answer", "").strip().lower()
            == record["tail"].strip().lower()
        )

        result = {
            "triple":        key,
            "true_tail":     record["tail"],
            "model_rank":    record["true_rank"],
            "agent_a":       a_out,
            "agent_b":       b_out,
            "score_a":       s_a,
            "score_b":       s_b,
            "aggregator":    agg,
            "final_correct": correct,
        }

        held_checkpoint[key] = result
        held_all_results.append(result)
        with open(HELD_OUT_CHECKPOINT, "w") as f:
            json.dump(held_checkpoint, f, indent=2)

    except Exception as e:
        print(f"  [ERROR] {key}: {e}")
        held_checkpoint[key] = {"error": str(e), "triple": key}
        with open(HELD_OUT_CHECKPOINT, "w") as f:
            json.dump(held_checkpoint, f, indent=2)

    if i < len(held_remaining) - 1:
        time.sleep(BETWEEN_RECORDS)

# ── save clean results ────────────────────────────────────
held_clean = [
    r for r in held_all_results
    if isinstance(r, dict)
    and "error" not in r
    and "score_a" in r
]

with open(HELD_OUT_RESULTS, "w") as f:
    json.dump(held_clean, f, indent=2)

held_correct = sum(1 for r in held_clean if r.get("final_correct"))
print(f"\n{'='*45}  HELD-OUT SUMMARY")
print(f"Processed:    {len(held_clean)}")
