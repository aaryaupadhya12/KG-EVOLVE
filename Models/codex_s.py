def load_tsv_memory(path=r"tsv_memory"):
    memory = defaultdict(list)

    if not os.path.exists(path):
        return memory

    with open(path) as f:
        reader = csv.DictReader(f, delimiter="\t")
        for row in reader:
            h = row["head"].strip()
            r = row["relation"].strip()
            t = row["tail"].strip()

            memory[h].append((r, t))   # ← REAL triples

    return memory

def get_memory_hint(head, memory, relation=None, k=5):
    triples = memory.get(head, [])

    if not triples:
        return ""

    # prioritize same relation if provided
    if relation is not None:
        same_rel = [(r, t) for r, t in triples if r == relation]
        others   = [(r, t) for r, t in triples if r != relation]
        selected = same_rel[:k] + others[:max(0, k - len(same_rel))]
    else:
        selected = triples[:k]

    # remove duplicates while preserving order
    seen = set()
    unique = []
    for r, t in selected:
        if (r, t) not in seen:
            seen.add((r, t))
            unique.append((r, t))

    return "\n".join(
        f"{head} --{r}--> {t}" for r, t in unique
    )

