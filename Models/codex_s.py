def verify_path(path_str: str, graph: dict) -> dict:
    if not path_str or str(path_str).lower() in ["none", "null", "", "false"]:
        return {"verified": False, "reason": "no path claimed"}
    import re
    nodes = re.split(r'\s*--[^>]+-->\s*', path_str)
    rels  = re.findall(r'--(.+?)-->', path_str)
    if len(nodes) < 2 or len(rels) < 1:
        return {"verified": False, "reason": "unparseable"}
    nodes = [n.strip() for n in nodes]
    rels  = [r.strip() for r in rels]
    for src, rel, dst in zip(nodes, rels, nodes[1:]):
        edge_exists = any(r == rel and t == dst for r, t in graph.get(src, []))
        if not edge_exists:
            return {
                "verified": False,
                "reason":   f"edge ({src} --{rel}--> {dst}) not in graph",
                "nodes":    nodes, "rels": rels,
            }
    return {"verified": True, "reason": "confirmed",
            "nodes": nodes, "rels": rels, "hops": len(rels)}


def verify_relations(claimed_rels: list, head: str,
                     tail: str, df_ref: pd.DataFrame) -> dict:
    if not claimed_rels:
        return {"verified": [], "hallucinated": [], "rate": 0.0}
    actual = set(df_ref[
        (df_ref["head"] == head) & (df_ref["tail"] == tail)
    ]["relation"])
    verified     = [r for r in claimed_rels if r in actual]
    hallucinated = [r for r in claimed_rels if r not in actual]
    return {
        "verified":     verified,
        "hallucinated": hallucinated,
        "rate":         len(hallucinated) / max(len(claimed_rels), 1),
    }


def trim_subgraph(subgraph: list, head: str, true_tail: str,
                  predicted: str, only_tail_has: list,
                  max_triples: int = 12) -> list:
    """
    Rank and trim subgraph to max_triples.
    Priority:
      1. Triples containing only_tail_has relations (gold signal)
      2. Triples directly involving true_tail or predicted
      3. Triples involving head
      4. Everything else (dropped first)
    """
    tier1, tier2, tier3, tier4 = [], [], [], []
    oth_set = set(only_tail_has)

    for triple in subgraph:
        if isinstance(triple, (list, tuple)) and len(triple) == 3:
            h, r, t = triple
        elif isinstance(triple, dict):
            h, r, t = triple.get("head",""), triple.get("relation",""), triple.get("tail","")
        else:
            continue

        if r in oth_set:
            tier1.append(triple)
        elif t in (true_tail, predicted) or h in (true_tail, predicted):
            tier2.append(triple)
        elif h == head or t == head:
            tier3.append(triple)
        else:
            tier4.append(triple)

    ranked = tier1 + tier2 + tier3 + tier4
    return ranked[:max_triples]


def build_subgraph_str(record: dict, max_triples: int = 12) -> str:
    subgraph  = record.get("subgraph", [])
    true_tail = record.get("true_tail") or record.get("tail", "")
    predicted = record.get("predicted", "")
    only_tail = record.get("only_tail_has", [])

    trimmed = trim_subgraph(
        subgraph, record["head"], true_tail,
        predicted, only_tail, max_triples
    )

    if not trimmed:
        return "  (no subgraph available)"

    lines = []
    for triple in trimmed:
        if isinstance(triple, (list, tuple)) and len(triple) == 3:
            h, r, t = triple
        elif isinstance(triple, dict):
            h = triple.get("head","?")
            r = triple.get("relation","?")
            t = triple.get("tail","?")
        else:
            continue
        # mark gold signal triples visually
        marker = " ◆" if r in set(only_tail) else ""
        lines.append(f"  {h} --{r}--> {t}{marker}")

    return "\n".join(lines)


def build_agent_context(record: dict, tsv_memory=None) -> str:

    # normalise keys
    true_tail = record.get("true_tail") or record.get("tail", "")
    predicted = record.get("predicted", "unknown")
    only_tail = record.get("only_tail_has", [])
    only_pred = record.get("only_pred_has", [])

    # memory prior
    memory_block = ""
    if tsv_memory:
        hint = get_memory_hint(record["head"], tsv_memory)
        if hint:
            memory_block = f"\nMemory prior for {record['head']}:\n{hint}\n"

    # separator block — gold signal at top, impossible to miss
    if only_tail:
        separator_block = (
            f"⚠ SEPARATING RELATIONS — cite these or state you cannot:\n"
            + "\n".join(f"  ◆ {r}" for r in only_tail)
            + f"\nThese connect [{record['head']}] → [{true_tail}] "
            f"but NOT → [{predicted}].\n"
        )
    else:
        separator_block = (
            f"⚠ NO SEPARATING RELATIONS in subgraph.\n"
            f"Forbidden (connect head to WRONG entity — do not cite):\n"
            + ("\n".join(f"  ✗ {r}" for r in only_pred) or "  none")
            + "\nYou must find a multi-hop path OR output confidence 0.3.\n"
        )

    # trimmed subgraph — ◆ marks gold-signal triples
    subgraph_str = build_subgraph_str(record)

    # sim summary — trim to 3 most similar only
    sim_head = record.get("sim_head", "")
    if sim_head:
        # keep only first 3 entries to reduce noise
        parts = sim_head.split(", ")
        sim_head = ", ".join(parts[:3])

    context = f"""Triple: ({record['head']}, {record['relation']}, ?)
Predicted wrong: {predicted}  |  rank of correct answer: {record['true_rank']}

{separator_block}
{memory_block}
Subgraph (◆ = gold signal triple — use these first):
{subgraph_str}

Similarity (top-3 embedding neighbours of head):
  {sim_head}

Hop type: {record.get('hop_type','multi')}
Failure: {record.get('fail_summary','')}"""

    return context

