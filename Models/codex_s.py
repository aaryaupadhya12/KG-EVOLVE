def failure_summary(head, relation, true_tail,
                    predicted_tail, model, df_train):
    # English label → integer ID via label_to_entity map
    h_id = entity_to_id[label_to_entity.get(head, head)]
    r_id = relation_to_id[label_to_relation.get(relation, relation)]
    t_id = entity_to_id[label_to_entity.get(true_tail, true_tail)]
    p_id = entity_to_id[label_to_entity.get(predicted_tail, predicted_tail)]

    score_true = model.score_hrt(
        torch.tensor([[h_id, r_id, t_id]])
    ).item()
    score_pred = model.score_hrt(
        torch.tensor([[h_id, r_id, p_id]])
    ).item()

    # df_train already has English strings so filtering works directly
    true_rels = set(
        df_train[
            (df_train["head"] == head) &
            (df_train["tail"] == true_tail)
        ]["relation"]
    )
    pred_rels = set(
        df_train[
            (df_train["head"] == head) &
            (df_train["tail"] == predicted_tail)
        ]["relation"]
    )

    shared    = true_rels & pred_rels
    only_true = true_rels - pred_rels
    only_pred = pred_rels - true_rels

    # English strings throughout — no resolve_labels() needed anywhere
    summary = (
        f"Model predicted '{predicted_tail}' "
        f"(score={score_pred:.3f}) over '{true_tail}' "
        f"(score={score_true:.3f}). "
        f"Shared relations: {len(shared)}. "
        f"Only '{true_tail}' has: "
        f"{', '.join(only_true) if only_true else 'none'}. "
        f"Only '{predicted_tail}' has: "
        f"{', '.join(only_pred) if only_pred else 'none'}."
    )

    return summary, {
        "score_true": score_true,
        "score_pred": score_pred,
        "shared":     shared,
        "only_true":  only_true,
        "only_pred":  only_pred,
    }

