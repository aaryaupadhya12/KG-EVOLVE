def preprocess_all_triples(model, df, split_name, df_reference):
    """
    Runs get_full_ranking_filtered on every triple.
    Stores rank, predicted, score, failure info.
    All splits. Run once. Never again.

    df_reference: training dataframe used for relation analysis
                  pass df_train for CoDEx-S
    """
    records = []
    total   = len(df)
    df      = df.reset_index(drop=True)

    for i, row in df.iterrows():
        head     = row["head"]
        relation = row["relation"]
        tail     = row["tail"]

        if relation not in relation_to_id:
            continue
        if head not in entity_to_id:
            continue
        if tail not in entity_to_id:
            continue

        ranking = get_full_ranking_filtered(
            head, relation, tail, model
        )

        sim_head, _ = similarity_summary(head, model, k=5)
        sim_tail, _ = similarity_summary(tail, model, k=5)

        subgraph = focused_subgraph(
            [head, tail, ranking["predicted"]],
            graph, hops=1
        )

        hop_type, hops, detail = hop_classifier(
            head, tail, graph,
            target_relation=relation
        )

        fail_summary, fail_raw = failure_summary(
            head, relation, tail, ranking["predicted"], model, df_reference
        )

        record = {
            "split":            split_name,
            "head":             head,
            "relation":         relation,
            "tail":             tail,
            "true_rank":        ranking["true_rank"],
            "predicted":        ranking["predicted"],
            "score_true":       ranking["true_score"],
            "score_predicted":  ranking["predicted_score"],
            "top5":             ranking["full_ranking"][:5],
            "hop_type":         hop_type,
            "hop_count":        hops,
            "sim_head":         sim_head,
            "sim_tail":         sim_tail,
            "fail_summary":     fail_summary,
            "subgraph":         subgraph,
            "shared_relations": list(fail_raw["shared"]),
            "only_tail_has":    list(fail_raw["only_true"]),
            "only_pred_has":    list(fail_raw["only_pred"]),
            "hard_failure":     ranking["true_rank"] > 3,
        }

        records.append(record)

        if (i + 1) % 50 == 0:
            print(f"  {split_name}: {i+1}/{total} done")

    return records

