def inference_deciding_signal(s_a: dict, s_b: dict,
                               a_out: dict, b_out: dict,
                               chosen: str, record: dict) -> str:
    """
    Clean deciding signal for inference.
    Uses only cited_count, path_found, rel_match, confidence.
    No quality_score, no contamination, no true_tail.
    """
    a_cited     = s_a.get("cited_count", 0)
    b_cited     = s_b.get("cited_count", 0)
    a_conf      = s_a.get("confidence",  0.0)
    b_conf      = s_b.get("confidence",  0.0)
    b_path      = s_b.get("path_found",  False)
    b_rel_match = s_b.get("rel_match",   False)
    a_rels      = s_a.get("agent_relations", [])
    b_rels      = s_b.get("agent_relations", [])
    b_path_str  = b_out.get("path_found", "none")

    if chosen == "B":
        # rule 1 — path + rel_match, check if conf is low
        if b_path and b_rel_match:
            conf_note = (f", note: B confidence low ({b_conf}) "
                        f"vs A confidence ({a_conf})"
                        if b_conf < 0.5 else "")
            return (f"Agent B found path {b_path_str} "
                    f"with path_relation_matches_query=True — "
                    f"B cited {b_cited} relations {b_rels}, "
                    f"A cited {a_cited} relations {a_rels}"
                    f"{conf_note}")

        # rule 2 — path but no rel_match
        if b_path and not b_rel_match:
            return (f"Agent B found path {b_path_str} "
                    f"and cited {b_cited} relations {b_rels} "
                    f"vs Agent A citing {a_cited} relations {a_rels}, "
                    f"path relation does not exactly match query relation")

        # rule 4 — more citations
        if b_cited > a_cited:
            return (f"No path found — Agent B cited more relations "
                    f"({b_cited}: {b_rels}) vs Agent A "
                    f"({a_cited}: {a_rels})")

        # rule 5 — multi-hop default
        return (f"Confidence equal (A={a_conf} B={b_conf}), "
                f"citations equal (A={a_cited} B={b_cited}), "
                f"multi-hop query defaults to Agent B — "
                f"B relations: {b_rels} vs A relations: {a_rels}")

    else:  # chosen == A
        # rule 1b — B had path+match but low confidence, A higher conf
        if b_path and b_rel_match and a_conf > b_conf:
            return (f"Agent B found path {b_path_str} with rel_match=True "
                    f"but low confidence ({b_conf}) — "
                    f"Agent A confidence ({a_conf}) higher, "
                    f"A cited {a_rels}")

        # rule 3 — higher confidence
        if a_conf > b_conf:
            return (f"Agent A confidence ({a_conf}) higher than "
                    f"B ({b_conf}) — A cited {a_cited} "
                    f"relations {a_rels}")

        # rule 4 — more citations
        if a_cited > b_cited:
            return (f"No path found — Agent A cited more relations "
                    f"({a_cited}: {a_rels}) vs Agent B "
                    f"({b_cited}: {b_rels})")

        # single hop default
        return (f"Single-hop query defaults to Agent A — "
                f"A relations: {a_rels} vs B relations: {b_rels}")


