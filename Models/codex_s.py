def _python_route(s_a: dict, s_b: dict, record: dict) -> str:
    """
    Deterministic routing — uses quality_score and contamination.
    Legitimate in training phase — scorer has true_tail.
    """
    qa = s_a["quality_score"]
    qb = s_b["quality_score"]
    ca = s_a["contamination"]
    cb = s_b["contamination"]

    # rule 1 — clear quality gap
    if qa > 0.5 and qb <= 0.5:
        return "A"
    if qb > 0.5 and qa <= 0.5:
        return "B"

    # rule 2 — both have quality, pick lower contamination
    if qa > 0 and qb > 0:
        if ca < cb:
            return "A"
        if cb < ca:
            return "B"

    # rule 3 — quality equal, use hop type preference
    if record["hop_type"] != "single":
        return "B"
    return "A"

